def add_numbers(a, b):
    return a + b

# Example usage:
num1 = 5
num2 = 3
result = add_numbers(num1, num2)
print("The sum of", num1, "and", num2, "is:", result)



def subtract_numbers(a, b):
    return a - b

# Example usage:
num1 = 5
num2 = 3
result = subtract_numbers(num1, num2)
print("The diff of", num1, "and", num2, "is:", result)


